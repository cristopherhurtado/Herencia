public class ClassPadre {
    private String nombres;
    private String Apellidos;
    private int edad;
    
    public ClassPadre(){
        this.nombres= "";
        this.Apellidos= "";
        this.edad= 0;
    }
    public ClassPadre(String nombres, String apellidos, int edad){
        this.nombres= nombres;
        this.Apellidos= apellidos;
        this.edad= edad;
    }
    
}
